
//Simple Code of addition using input of two numbers
import java.util.*;

public class InputSum {
    public static void main(String args[]) {
        Scanner sc = new Scanner(System.in);
        int A = sc.nextInt();
        int B = sc.nextInt();
        int sum = A + B;
        System.out.println("Sum of two number is= " + sum);
    }
}
