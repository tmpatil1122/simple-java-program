//simple Data Types in Java
public class Data {
    public static void main(String args[]) {
        byte b = 7;
        System.out.println(b);
        char ch = 't';
        System.out.println(ch);
        boolean var = true;
        System.out.println(var);
        float price = 7.5f;
        System.out.println(price);
        int number = 10;
        System.out.println(number);
        // long
        // double
        short n = 240;
        System.out.println(n);
    }

}
